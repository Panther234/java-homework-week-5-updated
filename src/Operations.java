public class Operations {
    public static void main(String[] args) {
        // A Java programme to print Operations of using different variables

        int a = 5;
        int b = 8;
        int c = 6;
        int d = 55;
        int e = 9;
        int f = 20;
        int g = 3;
        int h = 15;
        int i = 2;

        System.out.println(-a + b * c); // 43
        System.out.println((d + e) % e); // 1
        System.out.println(f + -g * a/b); // 19
        System.out.println(a + h/g * i - b % g); //13
    }
}
